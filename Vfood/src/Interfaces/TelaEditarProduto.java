    private void btnEditarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarProdutoActionPerformed
        if (txtNomeProduto.getText().equals("") || txtPrecoProduto.getText().equals("") 
            || txtQuantidadeTotalProduto.getText().equals("") || jComboBoxCategoriaProduto.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Verifique todos os campos!!!", "Campos em branco!!!", HEIGHT);
        } else {
            int confirmarEdicao = JOptionPane.showConfirmDialog(rootPane, "Realmente deseja editar este produto?", "Confirmar Edição", JOptionPane.YES_NO_OPTION);
            if (confirmarEdicao == JOptionPane.YES_OPTION) {
                String nomeProduto = txtNomeProduto.getText();
                String categoriaProduto = jComboBoxCategoriaProduto.getSelectedItem().toString();
                double precoProduto = Double.parseDouble(txtPrecoProduto.getText());
                int qtdProdutoTotal = Integer.parseInt(txtQuantidadeTotalProduto.getText());           
                produto.editarProduto(nomeProduto, categoriaProduto, precoProduto, qtdProdutoTotal);      
                JOptionPane.showMessageDialog(rootPane, "Editado com sucesso!!!", "Sucesso!", HEIGHT);
                TelaFuncionario telaFuncionario = new TelaFuncionario();
                telaFuncionario.setVisible(true);
                dispose();
                limparCampos();
            } 
        }     
    }//GEN-LAST:event_btnEditarProdutoActionPerformed


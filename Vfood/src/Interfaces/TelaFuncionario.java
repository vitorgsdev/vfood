    private void btnEditarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarProdutoActionPerformed
        int linhaSelecionada = jTableListaProduto.getSelectedRow();
        int codigoProduto = (int) jTableListaProduto.getValueAt(linhaSelecionada, 0);
        TelaEditarProduto telaEditarProduto = new TelaEditarProduto(codigoProduto);
        telaEditarProduto.setVisible(true);
        dispose();
    }//GEN-LAST:event_btnEditarProdutoActionPerformed

    private void btnExcluirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirProdutoActionPerformed
        int linhaSelecionada = jTableListaProduto.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel) jTableListaProduto.getModel();  
        int confirmarRemocao = JOptionPane.showConfirmDialog(rootPane, "Realmente deseja remover este produto?", "Confirmar Remoção", JOptionPane.YES_NO_OPTION);
        if (confirmarRemocao == JOptionPane.YES_OPTION) {
            Produto.removerProduto(linhaSelecionada);
            dtm.removeRow(linhaSelecionada);
            JOptionPane.showMessageDialog(rootPane, "Removido com sucesso!!!", "Sucesso!!!", HEIGHT);
        }
        hablitarBotao();
    }//GEN-LAST:event_btnExcluirProdutoActionPerformed


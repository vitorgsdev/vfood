    public TelaFuncionario() {
        initComponents();
        this.setLocationRelativeTo(null);
        btnEditarProduto.setEnabled(false);
        btnExcluirProduto.setEnabled(false);
        if (Produto.listaProdutos.size() == 0) {
           Produto.preCadastroProduto();
        }
        DefaultTableModel dtmTableListaProduto = (DefaultTableModel) jTableListaProduto.getModel();
        for (int i = 0; i < Produto.listaProdutos.size(); i++) {
            dtmTableListaProduto.addRow(new Object[] {
            Produto.listaProdutos.get(i).getCodProduto(), Produto.listaProdutos.get(i).getNomeProduto(), Produto.listaProdutos.get(i).getCategoria(),
            Produto.listaProdutos.get(i).getPrecoProduto(), Produto.listaProdutos.get(i).getQtdProdutoTotal(), Produto.listaProdutos.get(i).getQtdProdutoDisponivel()
            });
        }
    }
    
    public void hablitarBotao() {
        btnCadastrarProduto.setEnabled(true);
        btnListaPedidos.setEnabled(true);
        btnEditarProduto.setEnabled(false);
        btnExcluirProduto.setEnabled(false);
    }
    
    public void desabilitarBotao() {
        btnCadastrarProduto.setEnabled(false);
        btnListaPedidos.setEnabled(false);
        btnEditarProduto.setEnabled(true);
        btnExcluirProduto.setEnabled(true);
    }

